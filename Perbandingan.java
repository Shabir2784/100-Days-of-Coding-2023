package perbandingan;

public class Perbandingan {

    public static void main(String[] args) {
        /*
        ==
        !=
        >
        >=
        <
        <=
        */
        int a = 20;
        int b = 10;
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a < b);
        System.out.println(a >= b);
                
    
    }
    
}
