package variabell;
public class Variabell {

    public static void main(String[] args) {
        //integer (int)
        int umur = 19;
        System.out.println("Umur = "+umur + " "+"tahun");
        //floating pint (float)
        float berat = (float) 79.9;
        System.out.println("Berat = "+berat +" "+ "kg");
        //double floading pint(double)
        double panjang = 56.7;
        System.out.println("Panjang = "+panjang +" "+"m");
        //String (String)
        String nama = "Shobir";
        System.out.println ("Nama : "+nama);
        //Karakter (Char)
        char gender = 'L';
        System.out.println ("gender = "+gender);
        //boolean (boolean)
        boolean sehat = true;
        System.out.println ("sehat = "+sehat);
    }
    
}
